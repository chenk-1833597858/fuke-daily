# 连接Qdrant
client = QdrantClient(host="127.0.0.1", port=6333)
try:
    client.delete_collection(COLLECTION)
    print(f"已删除旧集合 {COLLECTION}")
except:
    pass
client.create_collection(
    collection_name=COLLECTION,
    vectors_config=VectorParams(size=512, distance=Distance.COSINE),
)
print(f"已创建集合 {COLLECTION} (384维, Cosine)")

# 嵌入
print("加载嵌入模型 BAAI/bge-small-zh-v1.5...")
embedder = TextEmbedding(model_name="BAAI/bge-small-zh-v1.5")
print("模型加载完成!")

texts = [f"{c['果']}。因：{' '.join(c.get('因',[]))}。缘：{' '.join(c.get('缘',[]))}" for c in all_chains]
print(f"正在计算 {len(texts)} 条向量的嵌入...")
embeddings = list(embedder.embed(texts))
print("嵌入完成!")

# 上传
points = []
for i, (c, emb) in enumerate(zip(all_chains, embeddings)):
    points.append(PointStruct(
        id=i+1, vector=emb.tolist(),
        payload={
            "类型": c["类型"], "烈度": c["烈度"], "烈度评分": c["烈度评分"],
            "果": c["果"], "因": c.get("因", []), "缘": c.get("缘", []),
            "来源": c.get("来源", "原始库")
        }
    ))

for i in range(0, len(points), 50):
    batch = points[i:i+50]
    client.upsert(collection_name=COLLECTION, points=batch)
    print(f"  上传 {min(i+50, len(points))}/{len(points)}")

print(f"\n=== 导入完成 ===")
col = client.get_collection(COLLECTION)
print(f"集合状态: {col.status}")
print(f"点数: {col.points_count}")

scores = sorted(set(c["烈度评分"] for c in all_chains))
print(f"烈度覆盖: {min(scores)}-{max(scores)} ✅")

# 搜索验证
print(f"\n搜索「被丈夫冷落的妻子」:")
v = list(embedder.embed(["被丈夫冷落的妻子"]))[0]
r = client.search(collection_name=COLLECTION, query_vector=v.tolist(), limit=3)
for h in r:
    p = h.payload
    print(f"  [{p['烈度']}] {p['果']} (相似度: {round(h.score, 4)})")
